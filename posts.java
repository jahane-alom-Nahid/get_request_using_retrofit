package com.example.getrequest;

public class posts {
    String id;
    String title;
    String name;

    public posts(String id, String title, String name) {
        this.id = id;
        this.title = title;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getAge() {
        return title;
    }

    public String getName() {
        return name;
    }
}
